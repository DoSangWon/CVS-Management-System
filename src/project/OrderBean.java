package project;

public class OrderBean {
	public String Onum;
	public String Opnum;
	public String Opname;
	public String Opamount;
	public String Odate;
	public String Obranch;
	public String state;
	
	public String getOnum() {
		return Onum;
	}
	public void setOnum(String onum) {
		Onum = onum;
	}
	public String getOpnum() {
		return Opnum;
	}
	public void setOpnum(String opnum) {
		Opnum = opnum;
	}
	public String getOpname() {
		return Opname;
	}
	public void setOpname(String opname) {
		Opname = opname;
	}
	public String getOpamount() {
		return Opamount;
	}
	public void setOpamount(String opamount) {
		Opamount = opamount;
	}
	public String getOdate() {
		return Odate;
	}
	public void setOdate(String odate) {
		Odate = odate;
	}
	public String getObranch() {
		return Obranch;
	}
	public void setObranch(String obranch) {
		Obranch = obranch;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	
	
	
	
	
}
