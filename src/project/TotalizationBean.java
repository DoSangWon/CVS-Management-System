package project;

public class TotalizationBean {
	public String Tnum;
	public String Tbid;
	public String Trevenue;
	public String Texpenditure;
	public String Tdate;
	
	public String getTnum() {
		return Tnum;
	}
	public void setTnum(String tnum) {
		Tnum = tnum;
	}
	public String getTbid() {
		return Tbid;
	}
	public void setTbid(String tbid) {
		Tbid = tbid;
	}
	public String getTrevenue() {
		return Trevenue;
	}
	public void setTrevenue(String trevenue) {
		Trevenue = trevenue;
	}
	public String getTexpenditure() {
		return Texpenditure;
	}
	public void setTexpenditure(String texpenditure) {
		Texpenditure = texpenditure;
	}
	public String getTdate() {
		return Tdate;
	}
	public void setTdate(String tdate) {
		Tdate = tdate;
	}
}
